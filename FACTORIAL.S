	AREA    RESET, DATA, READONLY
     	EXPORT  __Vectors

__Vectors 
	DCD  0x40001000     ; stack pointer value when stack is empty
      DCD  Reset_Handler  ; reset vector
  
    	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
		
Reset_Handler
	LDR R0,=INPUT 				; R0 HOLDING ADDRESS TO INPUT VALUE
	LDR R1,=RESULT 				; R1 HOLDING ADDRESS TO RESULT
	LDR R3,[R0] 				; R3 HOLDING INPUT VALUE, USED AS COUNTER FOR REPETITIVE MULTIPLICATION
	LDR R4,=1					; R4 WILL HOLD FINAL RESULT
LABEL1	MUL R4,R3				;MULTIPLY R4 WITH R0 AND STORE RESULT IN R4
		SUBS R3,#1 				; COUNTER UPDATION
		BNE LABEL1
		STR R4,[R1]				;STORE FINAL RESULT
	
HERE  B HERE					; STAY HERE
	
INPUT DCD 5 					 ;INPUT VALUE FOR CALCULATING FACTORIAL	
	
	AREA MYDATA,DATA,READWRITE
RESULT DCD 0                      ; FACTORIAL VALUE
	END
